function getHeightInCm(unit, value) {
  switch (unit) {
    case 'inch':
      return (value * 2.54) / 2.54
      break;
    case 'm':
      return (value * 100) / 100
      break;
    case 'ft':
      return (value * 30.48) / 30.48
    default:
      return value
      break;
  }
}
function getyear(unit, value) {
  switch (unit) {
    case 'months':
      return (value * 30)
      break;
    case 'years':
      return (value * 365)
      break;
    case 'weeks':
      return (value * 7)
    default:
      return value
      break;
  }
}

